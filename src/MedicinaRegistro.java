    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        
        Connection con = null;
        
        try{
            con = getConection();
            ps = con.prepareStatement("INSERT INTO medicamento (nombre,dosis,forma) VALUES(?,?,?)");
            ps.setString(1, txt_nombre.getText());
            ps.setString(2, txt_dosis.getText());
            ps.setString(3, cbx_forma.getSelectedItem().toString());
            
            int res = ps.executeUpdate();
            
            if(res > 0){
                JOptionPane.showMessageDialog(null, "Medicamento guardado.");
                limpiarCajas();
            } else{
                JOptionPane.showMessageDialog(null, "Error al guardar.");
                limpiarCajas();
            }
            
            con.close();
            
        } catch(Exception e){
            System.err.println(e);
        }
        
    }//GEN-LAST:event_btn_guardarActionPerformed

